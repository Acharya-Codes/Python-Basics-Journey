#[start : stop : step]

name = "Acharya"
print(name[0])  #This will return the first element of my string which is A
print(name[0:7]) #This will return all the elements 
print(name[0:7:2]) #A (c) h (a) r (y) a   (basicalyy it will take 1 step ahead)
print(name[-1]) #The last a will be printed (this is negative indexing)